            <h3 class="mx-auto text-center text-danger">Hispanic Business Association History</h3>
                    <p>The Association of Hispanic Business Leaders, Inc. (AHBLE) was incorporated on October 15, 1990 with three specific purposes:</p>
                    <ol>
                        <li>Promoting economic development within the Hispanic community.</li>
                        <li>Fostering educational, social and civic commitment among all Hispanic persons regardless of sex and political or religious views.</li>
                        <li>Promoting awareness and acceptance as Hispanics in all business, professional, paraprofessional and industrial sectors by among other things, accentuating Hispanic achievements therein.</li>
                    </ol>
	  
                    <p>The original incorporators were Salvador Villegas, Charles Lopez, Edward Ramirez, Ismael Chavez, Juan Zamora and Richard G. Cortez. </p>
                    <p>The name of the organization was changed to the Hispanic Business Association (HBA) and 501 (c) (3) status was granted in October of 1995. 
                        Led by the visionary leadership of the Chairman of the Board, Ray Veloz, the first decade of our existence was full of significant achievements in membership growth, 
                        educational mentoring activities, economic development and scholarships for students wishing to attain college degrees. Our signature achievement was the grant from the 
                        Ford Foundation of $25,000 to bolster our efforts to look and act like a Chamber of Commerce which was first discussed in 1993.</p>
                    <p>By the end of the first decade of the new millennium and with the inauguration of the Idaho Hispanic Chamber of Commerce in 2008 the mission of the HBA was evolving into 
                        less of a Chamber role and more of an educational support role.<em><strong>Today our singular focus is generating funds to provide scholarships to Hispanic students from 
                        Idaho and Eastern Oregon. </strong> </em> </p>  
                        <p>Since 1992 scholarship funds have been generated through the Jalapeño Open golf tournament with $500 generated in that first year.
                            Our first scholarship of $500 was awarded in 1993 to Joey Barroso of Vallivue High School. Joey went on to graduate from Boise State University and much to
                            our surprise and joy he recently received his MBA from BSU!  Since 1994 the tournament has been hosted and sponsored by Metalcraft, Inc., a company owned by Richard 
                            Cortez. Rich was instrumental in the formation and success of the organization over the years as well as a passionate supporter of education for Hispanic youth. 
                            In 2004 Rich passed away from cancer and in his memory the scholarship program was renamed the Richard G. Cortez Scholarship Program. In 2019 the HBA awarded $22,500 
                            in scholarships to 17 students from Idaho. Since 1993 the HBA has awarded 467 scholarships valued at $422,000!  </p> 
                            <p>Our sole mission today is to continue to raise funds to offer the opportunity to succeed to students who may not otherwise have that option through the Richard G. 
                            Cortez Scholarship Program.</p>  

