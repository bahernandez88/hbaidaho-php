                    <p>
                        <strong>Alice Mondragón Whitney</strong> retired in August 2018 from Zions Bank after over 30 years of real estate and mortgage banking experience, is fluent in Spanish and has provided Idaho women and minority entrepreneurs with invaluable counseling services in personal financial education and business planning.

Ms. Whitney was Director of Business Development with Sage Community Resources assisting more than 480 southwestern Idaho companies.  As a partner with the Small Business Administration she was instrumental in bringing to Idaho the first Women’s Network for Entrepreneurial Training (WNET) program in 1995, which led to the first Women’s Business Trade Show and development of Women’s Mentoring Systems, Inc., which now is responsible for the Women’s Business Center.  A partnership with the Hispanic Business Association has led to development of bilingual business training materials, workshops and conferences for Hispanic and minority businesses as well as creation of a “La Placita Marketplace” where Hispanic businesses can showcase their skills and services while learning the intricacies of administering a viable business.  The Hispanic Business Association received seed capital for a Hispanic Enterprise Loan Program (HELP) that provided start up and gap financing to new and existing companies, while requiring that these companies develop a sound business plan with implementation goals in place. Ms. Whitney has represented the State of Idaho at the invitation of the Governor and Senator Mike Crapo to the National Hispanic Opportunities Conference in Washington D.C.

As an entrepreneur Ms Whitney was the owner of the first woman owned aviation instrument facility on the West Coast.  Never underestimating her competition and working with a realistic set of goals she took her company into the world of commercial and corporate aviation, finding a niche of the industry that was not being served. Her company grew to be a well-respected major player in aviation nationally and internationally with a client base of companies such as, Forbes, Federal Express, John Deere, Amway Corporation, Boise Cascade, State of California, U.S. Air Force, Lockheed, Northrop, Garrett AirReasearch, American Airlines, Eastern Airlines, Boeing Seattle, Falcon Jet (France), Air Bus (France) and Smith Industries (England), British Aerospace (BAC-111).  Upon the sale of her company a career change was in order, an opportunity to enter the world of Real Estate, sales and mortgage lending was taken and soon Ms. Whitney found herself a Million Dollar producer with a Century 21 franchisee in southern California.  

Ms. Whitney believes that development of goals and the perception of how to accomplish them is the key to any idea, focus and understanding of the support mechanisms available whether monetary or personal, however are essential in starting a business and are also required to successfully counsel an entrepreneur.

